import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

king=pd.read_csv('/content/2025_Python1_King_house.csv')

king_zip = pd.read_csv('/content/2025_Python3_King_zip.csv')

merged_df=pd.merge(king,king_zip,on=['id','date'],how='left')

merged_df.id.value_counts()

merged_df.sort_values(by=['id','date'],inplace=True)
merged_df.drop_duplicates(subset=['id'],keep='last',inplace=True)
merged_df['m_price']=merged_df['price']/1000000
merged_df['age']=2025-merged_df['yr_built']
merged_df.loc[merged_df.yr_renovated!=0,'age']=2025-merged_df.yr_renovated
merged_df['date']=pd.to_datetime(merged_df.date.str[:8],format='%Y%m%d')

