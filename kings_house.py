import statsmodels.formula.api as smf
reg1='''m_price~bedrooms+bathrooms+sqft_living+sqft_above+sqft_basement+sqft_lot+floors+waterfront+condition+age'''
reg1_output=smf.ols(reg1,data=merged_df).fit()
print(reg1_output.summary())

reg2='''m_price~bedrooms+bathrooms+sqft_living+sqft_above+sqft_basement+sqft_lot+floors+waterfront+condition+age+C(zipcode)'''
reg2_output=smf.ols(reg2,data=merged_df).fit()
print(reg2_output.summary())

